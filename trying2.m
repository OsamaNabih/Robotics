% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

%Taking input
l1=str2num(get(handles.edit1,'string'));
l2=str2num(get(handles.edit2,'string'));
l3=str2num(get(handles.edit3,'string'));
q1max=str2num(get(handles.edit4,'string'));
q2max=str2num(get(handles.edit5,'string'));
q3max=str2num(get(handles.edit6,'string'));
q1min=str2num(get(handles.edit7,'string'));
q2min=str2num(get(handles.edit8,'string'));
q3min=str2num(get(handles.edit9,'string'));
a=str2num(get(handles.edit10,'string'));
b=str2num(get(handles.edit11,'string'));
theita=str2num(get(handles.edit12,'string'));
%Calling Inverse functions, which returns a vector of q1 with +ve alpha
%and -ve alpha then q2 with +ve alpha and -ve alpha and so on
Q = Inverse(l1,l2,l3,a,b,theita,q1max,q1min,q2max,q2min,q3max,q3min);

%Checking if solution is real (if complex then point can't be reached)
if (isreal(Q(1)) && isreal(Q(3)) && isreal(Q(5)))
    %Checking if the real solution is in permitted range
    if (Q(1) <= q1max && Q(1) >= q1min && Q(3) >= q2min && Q(3) <= q2max && Q(5) >= q3min && Q(5) <= q3max)
        set(handles.text2,'string',Q(1));
        set(handles.text3,'string',Q(3));
        set(handles.text4,'string',Q(5));
    else
           set(handles.text2,'string','Sol not in allowed range');
           set(handles.text3,'string','Sol not in allowed range');
           set(handles.text4,'string','Sol not in allowed range');
    end
else
    set(handles.text2,'string','No sol');
    set(handles.text3,'string','No sol');
    set(handles.text4,'string','No sol');
end

%Same thing as above
%{
if (q1min > q1max)
    q1max = q1max + 360;
end
if (q2min > q2max)
    q2max = q2max + 360;
end
if (q3min > q3max)
    q3max = q3max + 360;
end
%}
if (isreal(Q(2)) && isreal(Q(4)) && isreal(Q(6)))
    if (Q(2) <= q1max && Q(2) >= q1min && Q(4) >= q2min && Q(4) <= q2max && Q(6) >= q3min && Q(6) <= q3max)
        set(handles.text7,'string',Q(2));
        set(handles.text8,'string',Q(4));
        set(handles.text9,'string',Q(6));
    else
           set(handles.text7,'string','Sol not in allowed range');
           set(handles.text8,'string','Sol not in allowed range');
           set(handles.text9,'string','Sol not in allowed range');
    end
else
    set(handles.text7,'string','No sol');
    set(handles.text8,'string','No sol');
    set(handles.text9,'string','No sol');
end
